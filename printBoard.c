void printBoard() {
    printf("\"  ");
    for (int j = 1; j <= 15; j ++) {
        printf(" %c", 'A' - 1 + j);
    }
    printf("\\n\",\n");
    for (int i = 15; i >= 1; i --) {
        printf("\"%2d", i);
        for (int j = 1; j <= 15 ; j ++) {
            if (i == 15) {
                if (j == 1) {
                    printf(" ┌");
                } else if (j == 15) {
                    printf("─┐");
                } else {
                    printf("─┬");
                }
            } else if (i == 1) {
                if (j == 1) {
                    printf(" └");
                } else if (j == 15) {
                    printf("─┘");
                } else {
                    printf("─┴");
                }
            } else {
                if (j == 1) {
                    printf(" ├");
                } else if (j == 15) {
                    printf("─┤");
                } else {
                    printf("─┼");
                }
            }
        }
        printf("\\n\",\n");
    }
    return;
}


